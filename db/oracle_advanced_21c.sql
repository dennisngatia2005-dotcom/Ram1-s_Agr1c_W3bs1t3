-- 8a. AUDIT TRIGGER — logs every INSERT/UPDATE/DELETE on "member_table"
--     to a simple audit table.

CREATE TABLE "memberAuditLog_table" (
    "auditID"    NUMBER        GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "action"     VARCHAR2(10)  NOT NULL,   -- INSERT / UPDATE / DELETE
    "memberID"   NUMBER,
    "changedBy"  VARCHAR2(60)  DEFAULT USER,
    "changeTime" TIMESTAMP     DEFAULT SYSTIMESTAMP,
    "oldEmail"   VARCHAR2(100),
    "newEmail"   VARCHAR2(100),
    "oldIsActive" NUMBER(1),
    "newIsActive" NUMBER(1),
    CONSTRAINT "pk_memberAuditLog" PRIMARY KEY ("auditID")
);

CREATE OR REPLACE TRIGGER "trg_member_audit"
AFTER INSERT OR UPDATE OR DELETE ON "member_table"
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO "memberAuditLog_table" ("action","memberID","newEmail","newIsActive")
        VALUES ('INSERT', :NEW."memberID", :NEW."email", :NEW."isActive");

    ELSIF UPDATING THEN
        INSERT INTO "memberAuditLog_table"
            ("action","memberID","oldEmail","newEmail","oldIsActive","newIsActive")
        VALUES ('UPDATE', :NEW."memberID",
                :OLD."email", :NEW."email",
                :OLD."isActive", :NEW."isActive");

    ELSIF DELETING THEN
        INSERT INTO "memberAuditLog_table" ("action","memberID","oldEmail","oldIsActive")
        VALUES ('DELETE', :OLD."memberID", :OLD."email", :OLD."isActive");
    END IF;
END;
/

-- 8b. PAYMENT AUDIT TRIGGER — writes to the blockchain table whenever
--     a payment is inserted (can't update/delete blockchain rows).

CREATE OR REPLACE TRIGGER "trg_payment_blockchain"
AFTER INSERT ON "payment_table"
FOR EACH ROW
BEGIN
    INSERT INTO "paymentAuditLedger_table"
        ("paymentID","memberID","amount","paymentType","notes")
    VALUES
        (:NEW."paymentID", :NEW."memberID", :NEW."amount",
         :NEW."paymentType",
         'Auto-recorded by payment trigger');
END;
/

-- 8c. BORROW HISTORY TRIGGER — copies completed borrows to the
--     immutable borrowHistory table when returnDate is set.

CREATE OR REPLACE TRIGGER "trg_borrow_archive"
AFTER UPDATE OF "returnDate" ON "borrow_table"
FOR EACH ROW
WHEN (NEW."returnDate" IS NOT NULL AND OLD."returnDate" IS NULL)
DECLARE
    v_memberName   VARCHAR2(62);
    v_resourceTitle VARCHAR2(255);
BEGIN
    SELECT TRIM(m."firstName") || ' ' || TRIM(m."secondName")
    INTO   v_memberName
    FROM   "member_table" m WHERE m."memberID" = :NEW."memberID";

    SELECT r."title"
    INTO   v_resourceTitle
    FROM   "resource_table" r WHERE r."resourceID" = :NEW."resourceID";

    INSERT INTO "borrowHistory_table"
        ("borrowID","memberID","resourceID","memberName",
         "resourceTitle","borrowDate","returnDate")
    VALUES
        (:NEW."borrowID", :NEW."memberID", :NEW."resourceID",
         v_memberName, v_resourceTitle,
         :NEW."borrowDate", :NEW."returnDate");
END;
/

-- 8d. STOCK GUARD TRIGGER — extra safety net; raises error if
--     something tries to set availableCopies below zero.

CREATE OR REPLACE TRIGGER "trg_resource_stock_guard"
BEFORE UPDATE OF "availableCopies" ON "resource_table"
FOR EACH ROW
BEGIN
    IF :NEW."availableCopies" < 0 THEN
        RAISE_APPLICATION_ERROR(-20010,
            'availableCopies cannot go below 0 for resource: '
            || :NEW."resourceID");
    END IF;
END;
/

-- 8e. TIMESTAMP GUARD TRIGGER — prevents borrowDate being set in the future

CREATE OR REPLACE TRIGGER "trg_borrow_date_guard"
BEFORE INSERT ON "borrow_table"
FOR EACH ROW
BEGIN
    IF :NEW."borrowDate" > SYSDATE + 1 THEN
        RAISE_APPLICATION_ERROR(-20011,
            'borrowDate cannot be more than 1 day in the future.');
    END IF;
END;
/


-- ================================================================
-- SECTION 9 — SQL MACROS (21c)
-- ================================================================
-- SQL Macros allow you to define reusable SQL fragments as PL/SQL
-- functions that are inlined by the optimizer at parse time —
-- zero runtime overhead, unlike pipelined functions.

-- 9a. SCALAR SQL Macro — returns age in years of a member_table
CREATE OR REPLACE FUNCTION member_tenure_years(p_joinDate DATE)
    RETURN VARCHAR2 SQL_MACRO(SCALAR)
IS
BEGIN
    RETURN q'[
        ROUND(MONTHS_BETWEEN(SYSDATE, p_joinDate) / 12, 1)
    ]';
END;
/

-- 9b. TABLE SQL Macro — returns active borrows for a given member_table.
--     Called like a table: SELECT * FROM active_borrows_for(101)
CREATE OR REPLACE FUNCTION active_borrows_for(p_memberID NUMBER)
    RETURN VARCHAR2 SQL_MACRO(TABLE)
IS
BEGIN
    RETURN q'[
        SELECT b."borrowID",
               r."title"      AS resource_title,
               r."type"       AS resource_type,
               b."borrowDate"
        FROM   "borrow_table"   b
        JOIN   "resource_table" r ON b."resourceID" = r."resourceID"
        WHERE  b."memberID"   = p_memberID
          AND  b."returnDate" IS NULL
    ]';
END;
/

-- 9c. TABLE SQL Macro — revenue summary for a given date range
CREATE OR REPLACE FUNCTION revenue_in_period(
    p_from DATE, p_to DATE
) RETURN VARCHAR2 SQL_MACRO(TABLE)
IS
BEGIN
    RETURN q'[
        SELECT c."categoryName",
               COUNT(p."paymentID")  AS payment_count,
               SUM(p."amount")       AS total_amount,
               AVG(p."amount")       AS avg_amount,
               MIN(p."amount")       AS min_amount,
               MAX(p."amount")       AS max_amount
        FROM   "payment_table"  p
        JOIN   "member_table"   m ON p."memberID"   = m."memberID"
        JOIN   "category_table" c ON m."categoryID" = c."categoryID"
        WHERE  p."paymentDate" BETWEEN p_from AND p_to
        GROUP BY c."categoryName"
    ]';
END;
/


