SELECT s."showID", s."town", s."eventDate",
       b."branchName", b."location",
       s."isCancelled"
FROM   "show_table"   s
JOIN   "branch_table" b ON s."branchID" = b."branchID"
WHERE  s."eventDate"   >= SYSDATE
  AND  s."isCancelled"  = 0
ORDER BY s."eventDate";

-- V3. revenueByBranch — for bar chart
CREATE OR REPLACE VIEW "revenueByBranch" AS
SELECT b."branchName",
       NVL(SUM(p."amount"),0)         AS total_revenue,
       COUNT(DISTINCT m."memberID")   AS member_count
FROM   "branch_table"  b
LEFT JOIN "member_table"  m ON b."branchID" = m."branchID"
LEFT JOIN "payment_table" p ON m."memberID" = p."memberID"
GROUP BY b."branchName"
ORDER BY total_revenue DESC;

-- V4. announcementSummary — uses JSON_VALUE to surface metadata fields
CREATE OR REPLACE VIEW "announcementSummary" AS
SELECT a."announcementID",
       a."title",
       SUBSTR(a."body",1,200)                AS excerpt,
       a."postedDate",
       a."postedBy",
       b."branchName",
       JSON_VALUE(a."metadata",'$.priority') AS priority,
       JSON_VALUE(a."metadata",'$.tags')     AS tags
FROM   "announcement_table" a
LEFT JOIN "branch_table" b ON a."branchID" = b."branchID";

-- V5. membersPerBranch — member counts per branch
CREATE OR REPLACE VIEW "membersPerBranch" AS
SELECT b."branchName",
       COUNT(m."memberID")                                    AS totalMembers,
       SUM(CASE WHEN m."isActive" = 1 THEN 1 ELSE 0 END)     AS activeMembers
FROM   "branch_table" b
LEFT JOIN "member_table" m ON b."branchID" = m."branchID"
GROUP BY b."branchID", b."branchName"
ORDER BY totalMembers DESC;

-- V6. resourceUsage — borrow statistics per resource
CREATE OR REPLACE VIEW "resourceUsage" AS
SELECT r."resourceID",
       r."title",
       r."type",
       COUNT(b."borrowID")                                        AS totalBorrows,
