-- 8a. AUDIT TRIGGER — logs every INSERT/UPDATE/DELETE on member_table.
--     NOTE: Disable this trigger before any bulk member imports
--     to avoid row-by-row audit overhead:
--       ALTER TRIGGER "trg_member_audit" DISABLE;
--       -- bulk load here --
--       ALTER TRIGGER "trg_member_audit" ENABLE;

CREATE TABLE "memberAuditLog_table" (
    "auditID"     NUMBER        GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "action"      VARCHAR2(10)  NOT NULL,
    "memberID"    NUMBER,
    "changedBy"   VARCHAR2(60)  DEFAULT USER,
    "changeTime"  TIMESTAMP     DEFAULT SYSTIMESTAMP,
    "oldEmail"    VARCHAR2(100),
    "newEmail"    VARCHAR2(100),
    "oldIsActive" NUMBER(1),
    "newIsActive" NUMBER(1),
    CONSTRAINT "pk_memberAuditLog" PRIMARY KEY ("auditID")
);

CREATE OR REPLACE TRIGGER "trg_member_audit"
AFTER INSERT OR UPDATE OR DELETE ON "member_table"
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO "memberAuditLog_table" ("action","memberID","newEmail","newIsActive")
        VALUES ('INSERT', :NEW."memberID", :NEW."email", :NEW."isActive");

    ELSIF UPDATING THEN
        INSERT INTO "memberAuditLog_table"
            ("action","memberID","oldEmail","newEmail","oldIsActive","newIsActive")
        VALUES ('UPDATE', :NEW."memberID",
                :OLD."email", :NEW."email",
                :OLD."isActive", :NEW."isActive");

    ELSIF DELETING THEN
        INSERT INTO "memberAuditLog_table" ("action","memberID","oldEmail","oldIsActive")
        VALUES ('DELETE', :OLD."memberID", :OLD."email", :OLD."isActive");
    END IF;
END;
/

