-- SECTION 6 — PROPERTY GRAPH (21c)
-- ================================================================
-- Models members, branches, resources and borrows as a graph.
-- Enables graph traversal queries using GRAPH_TABLE and PGQL.

CREATE OR REPLACE PROPERTY GRAPH "agriGraph"
    VERTEX TABLES (
        "member_table"
            KEY ("memberID")
            LABEL Member
            PROPERTIES ("firstName", "secondName", "joinDate"),
        "resource_table"
            KEY ("resourceID")
            LABEL "Resource"
            PROPERTIES ("title", "type"),
        "branch_table"
            KEY ("branchID")
            LABEL Branch
            PROPERTIES ("branchName", "location")
    )
    EDGE TABLES (
        "borrow_table"
            KEY ("borrowID")
            SOURCE      KEY ("memberID")   REFERENCES "member_table"   ("memberID")
            DESTINATION KEY ("resourceID") REFERENCES "resource_table" ("resourceID")
            LABEL Borrowed
            PROPERTIES ("borrowDate", "returnDate"),
        "member_table"
            KEY ("memberID")
            SOURCE      KEY ("memberID") REFERENCES "member_table" ("memberID")
            DESTINATION KEY ("branchID") REFERENCES "branch_table" ("branchID")
            LABEL BelongsTo
            PROPERTIES ("joinDate")
    );


