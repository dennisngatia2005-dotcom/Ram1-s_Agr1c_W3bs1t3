-- ================================================================
-- SECTION 5 — PARTITIONED PAYMENTS ARCHIVE TABLE
-- ================================================================
-- Range partitioning by year so old payment data stays queryable
-- but is stored in separate physical segments. This dramatically
-- improves performance for year-based revenue queries.

CREATE TABLE "paymentArchive_table" (
    "archiveID"   NUMBER        GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "memberID"    NUMBER        NOT NULL,
    "amount"      NUMBER(10,2)  NOT NULL,
    "paymentType" VARCHAR2(30)  NOT NULL,
    "paymentDate" DATE          NOT NULL,
    "archivedAt"  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL
)
PARTITION BY RANGE ("paymentDate") INTERVAL (NUMTOYMINTERVAL(1,'YEAR')) (
    PARTITION "pmt_before_2023" VALUES LESS THAN (DATE '2023-01-01'),
    PARTITION "pmt_2023"        VALUES LESS THAN (DATE '2024-01-01'),
    PARTITION "pmt_2024"        VALUES LESS THAN (DATE '2025-01-01')
    -- Future years auto-created by INTERVAL clause
);

ALTER TABLE "paymentArchive_table"
    ADD CONSTRAINT "pk_paymentArchive" PRIMARY KEY ("archiveID", "paymentDate");


