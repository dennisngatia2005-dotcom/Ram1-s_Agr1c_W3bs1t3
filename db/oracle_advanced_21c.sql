       ROUND(AVG(b."returnDate" - b."borrowDate"), 1)             AS avgBorrowDays
FROM   "resource_table" r
LEFT JOIN "borrow_table" b ON r."resourceID" = b."resourceID"
GROUP BY r."resourceID", r."title", r."type"
ORDER BY totalBorrows DESC;

