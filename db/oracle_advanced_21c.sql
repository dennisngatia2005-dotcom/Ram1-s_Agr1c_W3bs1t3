-- ================================================================
-- SECTION 3 — BLOCKCHAIN TABLE (21c)
-- ================================================================
-- Blockchain tables are append-only, tamper-evident ledgers.
-- Each row is cryptographically chained to the previous one.
-- Rows CANNOT be updated or deleted — perfect for a payment audit trail.
-- NO DELETE is enforced for 31 days after last insert here.

CREATE BLOCKCHAIN TABLE "paymentAuditLedger_table" (
    "ledgerID"     NUMBER        GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "paymentID"    NUMBER        NOT NULL,
    "memberID"     NUMBER        NOT NULL,
    "amount"       NUMBER(10,2)  NOT NULL,
    "paymentType"  VARCHAR2(30)  NOT NULL,
    "actionTime"   TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    "actionBy"     VARCHAR2(60)  DEFAULT USER NOT NULL,
    "ipAddress"    VARCHAR2(45),
    "notes"        VARCHAR2(500)
)
NO DROP UNTIL 31 DAYS IDLE
NO DELETE UNTIL 31 DAYS AFTER INSERT
HASHING USING "SHA2_512" VERSION "v1";

-- Constraint added separately (blockchain tables have DDL restrictions)
ALTER TABLE "paymentAuditLedger_table"
    ADD CONSTRAINT "pk_paymentAuditLedger" PRIMARY KEY ("ledgerID");


